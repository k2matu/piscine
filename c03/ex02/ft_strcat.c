char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}

#include <stdio.h>
#include <string.h>
int main(void)
{
    char str1[100] = "Hello ";
    char str2[] = "World";

    ft_strcat(str1, str2);
    puts(str1);

    return (0);
} 
